"""Stoploss tab stress tests — 16 tests covering every per-slot SL/TP setting.

Targets the Exit Settings (stoploss) tab inputs surfaced in the slot/leg modal:

* ``stop_loss_type``  ∈ {none, percentage, points, trailing}
* ``stop_loss_value`` (numeric)
* ``trailing_sl_step`` / ``trailing_sl_offset`` (only for type=trailing)
* ``target_type``     ∈ {none, percentage, points}
* ``target_value``
* ``target_lock_trigger`` / ``target_lock_minimum`` (target lock SL ratchet)
* ``sl_wait_bars``    (delay-confirmation before honoring SL)
* ``on_sl_action``    ∈ {close, re_execute, reverse}
* ``on_target_action`` ∈ {close, re_execute, reverse}
* ``max_re_executions``

Each test builds a synthetic portfolio with deliberate parameter combinations,
runs the backtest, then verifies report invariants against the produced
orderbook (EXIT REASON, trade counts, PnL signs, re-entries, etc.).

Output: ``5. Logics/stoploss_tests/`` per the existing convention.
- ``stoploss_results.json``           — raw per-test summaries
- ``stoploss_report.html``            — generated by the companion report script

Run from the project root::

    python stress_test_stoploss.py
    python stress_test_stoploss_report.py
"""

