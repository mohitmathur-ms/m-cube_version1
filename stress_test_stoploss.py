def _slot(slot_id: str,
          bar_type: str = "EURUSD.FOREX_MS-1-MINUTE-MID-EXTERNAL",
          lots: float = 0.01,
          strategy: str = "EMA Cross",
          strategy_params: dict | None = None,
          start: str = DEFAULT_START,
          end: str = DEFAULT_END,
          **exit_overrides) -> dict:
    """Build a slot dict with a default exit_config plus any overrides."""
    ec: dict[str, Any] = {
        "stop_loss_type": "none", "stop_loss_value": 0.0,
        "trailing_sl_step": 0.0, "trailing_sl_offset": 0.0,
        "target_type": "none", "target_value": 0.0,
        "target_lock_trigger": None, "target_lock_minimum": None,
        "sl_wait_bars": 0,
        "on_sl_action": "close", "on_target_action": "close",
        "max_re_executions": 0,
        "squareoff_time": None, "squareoff_tz": None,
    }
    ec.update(exit_overrides)
    return {
        "slot_id": slot_id,
        "strategy_name": strategy,
        "strategy_params": strategy_params or {},
        "bar_type_str": bar_type,
        "lots": lots,
        "allocation_pct": 0.0,
        "exit_config": ec,
        "enabled": True,
        "start_date": start,
        "end_date": end,
    }


def _portfolio(name: str, slots: list[dict], **overrides) -> dict:
    pf: dict[str, Any] = {
        "name": name,
        "description": "stoploss tab stress portfolio",
        "starting_capital": 100_000.0,
        "allocation_mode": "equal",
        "start_date": DEFAULT_START,
        "end_date": DEFAULT_END,
        "slots": slots,
    }
    pf.update(overrides)
    return pf


