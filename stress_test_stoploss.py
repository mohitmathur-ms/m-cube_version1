    # ═══════════════════════════════════════════════════════════════════
    # S12 — sl_wait_bars: require 3 confirmation bars before honoring SL
    # SL price is hit but action waits sl_wait_bars=3. If price recovers
    # before 3 bars elapse, SL is cancelled. Should produce fewer SL exits
